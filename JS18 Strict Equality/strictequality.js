// = - assignment operator
// == - equality operator (compare if values are equal)
// === - strict equality operator (compare both if values & data types are equal)

// != - inequality operator
// !== - strict inequality operator

const PI = 3.14;

if(PI === "3.14")
{
    console.log("This is PI");
}
else{
    console.log("This is not PI");
}

if(PI !== "3.14")
{
    console.log("This is not PI");
}
else
{
    console.log("This is PI");
}